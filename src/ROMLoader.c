// Gets loader image and size
const uint8_t* GetCartridgeLoaderBytes(int in_loader_index, int* out_loader_length, int* out_compression)
{
	// select loader
	switch (in_loader_index)
	{
		case 0:
			*out_loader_length = l_loader_no_comp_no_border_size;
			*out_compression = 0;
			return l_loader_no_comp_no_border;

		case 1:
			*out_loader_length = l_loader_no_comp_border_size;
			*out_compression = 0;
			return l_loader_no_comp_border;

		case 2:
			*out_loader_length = l_loader_comp_no_border_size;
			*out_compression = 1;
			return l_loader_comp_no_border;

		case 3:
			*out_loader_length = l_loader_comp_border_size;
			*out_compression = 1;
			return l_loader_comp_border;
	}

	// no loader found
	*out_loader_length = 0;
	*out_compression = 0;
	return NULL;
}
